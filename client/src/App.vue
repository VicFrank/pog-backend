<template>
  <div id="app">
    <Header />
    <div class="main-layout" :key="loggedIn">
      <Sidebar />
      <div
        @click="onMainContentClicked"
        v-bind:class="{ 'sidebar-open': sidebarOpen, 'neutral-div': true }"
      >
        <router-view class="main-content"></router-view>
        <Footer />
      </div>
    </div>
  </div>
</template>

<script>
import Header from "./components/Header.vue";
import Sidebar from "./components/Sidebar.vue";
import Footer from "./components/Footer.vue";

export default {
  name: "app",
  computed: {
    isRoot() {
      return this.$route.path === "/";
    },
    sidebarOpen() {
      return this.$store.state.ui.sidebarOpen;
    },
    loggedIn() {
      return this.$store.state.auth.loggedIn;
    }
  },
  methods: {
    onMainContentClicked() {
      if (this.sidebarOpen && window.innerWidth < 1200)
        this.$store.dispatch("toggleSidebar");
    }
  },
  created() {
    fetch("/api/auth/steam/success", { credentials: "include" })
      .then(res => res.json())
      .then(res => {
        if (res.success) {
          const { displayName, photos, id, isAdmin, poggers } = res.user;

          this.$store.commit({
            type: "setUser",
            username: displayName,
            steamID: id,
            picture: photos[2].value,
            isAdmin,
            poggers
          });

          this.$store.dispatch("refreshPlayer");
        } else {
          this.$store.commit({
            type: "setNotLoggedIn"
          });
        }
      });
  },
  components: {
    Header,
    Sidebar,
    Footer
  }
};
</script>

